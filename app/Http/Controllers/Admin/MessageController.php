     */
    public function store(MessagesRequest $request)
    {
        $message = new Message($request->all());
        $message->user_id = Auth::user()->id;
        $message->save();
        Notification::makeNotification('message','Tienes mensajes nuevos','admin/lista-mensajes');
        return redirect()->action('Admin\MessageController@index');
    }

